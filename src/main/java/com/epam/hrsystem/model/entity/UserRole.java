package com.epam.hrsystem.model.entity;

/**
 * Enumeration of users' roles.
 *
 * @author Aleksey Klevitov
 */
public enum UserRole {
    /**
     * Represents admin role.
     */
    ADMIN,
    /**
     * Represents applicant role.
     */
    APPLICANT,
    /**
     * Represents employee role.
     */
    EMPLOYEE,
    /**
     * Represents guest role.
     */
    GUEST
}
